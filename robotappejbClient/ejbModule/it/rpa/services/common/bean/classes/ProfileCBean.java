package it.rpa.services.common.bean.classes;

public class ProfileCBean {

	private String user;
	private String enablingCode;
	private String profile;

	public ProfileCBean() {

	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getEnablingCode() {
		return enablingCode;
	}

	public void setEnablingCode(String enablingCode) {
		this.enablingCode = enablingCode;
	}

	public String getProfile() {
		return profile;
	}

	public void setProfile(String profile) {
		this.profile = profile;
	}

}
